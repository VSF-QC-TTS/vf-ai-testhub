CREATE TABLE datasets (
    id                      BIGINT          GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    public_id               UUID            NOT NULL DEFAULT gen_random_uuid(),
    project_id              BIGINT          NOT NULL,
    name                    VARCHAR(255)    NOT NULL,
    description             TEXT,
    category                VARCHAR(100),
    tags                    JSONB           DEFAULT '[]',
    metadata                JSONB           DEFAULT '{}',
    created_by              BIGINT          NOT NULL,
    archived                BOOLEAN         NOT NULL DEFAULT FALSE,
    created_at              TIMESTAMPTZ     NOT NULL DEFAULT NOW(),
    updated_at              TIMESTAMPTZ     NOT NULL DEFAULT NOW(),

    CONSTRAINT uq_datasets_public_id UNIQUE (public_id),
    CONSTRAINT uq_datasets_project_name UNIQUE (project_id, name),
    CONSTRAINT fk_datasets_project FOREIGN KEY (project_id) REFERENCES projects(id) ON DELETE CASCADE,
    CONSTRAINT fk_datasets_created_by FOREIGN KEY (created_by) REFERENCES users(id)
);

CREATE INDEX idx_datasets_project_id ON datasets(project_id);
CREATE INDEX idx_datasets_created_by ON datasets(created_by);
CREATE INDEX idx_datasets_archived ON datasets(archived);
